something = input()

if something == 'banana' or something == 'apple' or something == 'kiwi' or something == 'cherry' \
        or something == 'lemon' or something == 'grapes':
    print('fruit')
elif something == 'tomato' or something == 'cucumber' or something == 'pepper' or something == 'carrot':
    print('vegetable')
else:
    print('unknown')
