day = int(input())

if day == 1:
    print(f'Monday')
elif day == 2:
    print(f'Tuesday')
elif day == 3:
    print(f'Wednesday')
elif day == 4:
    print(f'Thursday')
elif day == 5:
    print(f'Friday')
elif day == 6:
    print(f'Saturday')
elif day == 7:
    print(f'Sunday')
else:
    print(f'Error')

